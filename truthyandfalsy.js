/*TRUTHY AND FALSY IN JS*/

/*Falsy values in JS are : false,0,-0,"",null,undefined,NaN,etc..*/

if(true){
    console.log("it is TRUTHY")//dark in color
}
else{
    console.log("it is FALSY")
}

//output is "it is TRUTHY"//

if(false){
    console.log("it is TRUTHY")//light in color
}
else{
    console.log("it is FALSY")
}

//output is "it is falsy"//

if(1){
    console.log("it is TRUTHY")
}
else{
    console.log("it is FALSY")
}

//output is "it is TRUTHY"//

if(0){
    console.log("it is TRUTHY")
}
else{
    console.log("it is FALSY")
}

